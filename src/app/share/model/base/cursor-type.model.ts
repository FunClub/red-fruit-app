export class CursorType {
  /**
   * 普通光标
   * @type {number}
   */
  SIMPLE_CURSOR:number=0;

  /**
   * 上一张图片的光标
   * @type {number}
   */
  PREV_CURSOR:number=1;

  /**
   * 缩小图片的光标
   * @type {number}
   */
  ZOOM_OUT_CURSOR:number=2;

  /**
   * 下一张图片的光标
   * @type {number}
   */
  NEXT_CURSOR:number=3;
}
